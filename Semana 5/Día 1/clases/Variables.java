package clases;
public class Variables{
    public static void main(String args[]){
        // Tipos de datos
        int edad; // Enteros
        double calificacion; // Decimales
        String nombre; // Cadenas de caracteres
        boolean aproboExamen; // Boleano
        char sexo; // Caracter
        
        edad = 25;
        calificacion = 9.85;
        nombre = "Alex";
        sexo = 'M';
        aproboExamen = true;

        System.out.println(edad);
        System.out.println(calificacion);
        System.out.println(nombre);
        System.out.println(sexo);
        System.out.println(aproboExamen);
    }
}